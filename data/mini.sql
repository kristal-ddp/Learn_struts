CREATE TABLE MEMBER (
    USERID VARCHAR2(20),
    USERPWD VARCHAR2(20),
    USERNAME VARCHAR2(20),
    USERGENDER VARCHAR2(20),
    USERBIRTH DATE,
    USERADDR VARCHAR2(1000),
    USEREMAIL VARCHAR2(50),

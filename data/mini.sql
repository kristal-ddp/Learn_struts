CREATE TABLE BOARDS (
   BOARDNUM NUMBER,
   USERID VARCHAR2(20),
   SUBJECT VARCHAR2(100),
   CONTENT VARCHAR2(1024),
   POSTDATE DATE,
   HITCOUNT NUMBER,
   GROUPNUM NUMBER,
   ORDERNUM NUMBER,
   DEPTH NUMBER,
   PARENT NUMBER,
   CONSTRAINT PK_BOARDS_BOARDNUM PRIMARY KEY (BOARDNUM),
   CONSTRAINT FK_BOARDS_USERID FOREIGN KEY (USERID) REFERENCES MEMBER(USERID));

